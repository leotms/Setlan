# Archivo que define las funciones para la evaluacion de setlan

# Suma para dos enteros
def suma(x,y):
	return x + y

# Resta para dos enteros
def resta(x,y):
	return x - y

# Multiplicacion de dos enteros
def multiplicacion(x,y):
	return x*y

# Division entera de dos enteros.
def division(x,y):
	return x//y

# Modulo de la division entera de dos enteros.
def modulo(x,y):
	return x%y

# And logico 
def logicAnd(x,y):
	return x and y

# Or Logico
def logicOr(x,y):
	return x or y

# Menor  para enteros
def menor(x,y):
	return x < y

# Mayor para enteros
def mayor(x,y):
	return x > y

# Menor o igual para enteros
def menorIgual(x,y):
	return x <= y

# Mayor o igual para enteros
def mayorIgual(x,y):
	return x >= y

# Igualdad para enteros, conjuntos y booleanos
def igual(x,y):
	return x == y

# Desigualdad para enteros, conjuntos y booleanos
def desigual(x,y):
	return x != y
